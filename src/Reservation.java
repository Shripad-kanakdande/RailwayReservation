    public void load()
    {
        String splace = comBoarding.getSelectedItem().toString();
        String eplace = comDest.getSelectedItem().toString();
        try
        {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/railwayreservation", "root", "shripad3721");
            pst = con.prepareStatement("select * from traindetails where startplace = ? and destinationplace = ?");
            pst.setString(1, splace);
            pst.setString(2, eplace);
            rs = pst.executeQuery();
            
            if(rs.next() == true)
            {
                txtTrainNo.setText(rs.getString(1));
                txtTrainName.setText(rs.getString(2));
                txtPrice.setText(rs.getString(5));
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record NOt Found!");
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }

        
        
        
        
        

