    private void btnBookActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnBookActionPerformed
    {//GEN-HEADEREND:event_btnBookActionPerformed
        // TODO add your handling code here:
        
        String pno = txtPno.getText();
        String splace = comBoarding.getSelectedItem().toString();
        String eplace = comDest.getSelectedItem().toString();
        String tno = txtTrainNo.getText();
        String tname = txtTrainName.getText();
        String price = txtPrice.getText();
        String Date = txtDate.getText();
        String ticket = txtTickets.getText();
        String total = txtTotal.getText();

    try {
        java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/railwayreservation", "root", "shripad3721");
        String query = "INSERT INTO reservation(pname, splace, eplace, tno, tname, price, Jdate, ticket, total) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try (PreparedStatement pst = con.prepareStatement(query)) {
            pst.setString(1, pno);
            pst.setString(2, splace);
            pst.setString(3, eplace);
            pst.setString(4, tno);
            pst.setString(5, tname);
            pst.setString(6, price);
            pst.setString(7, Date);
            pst.setString(8, ticket);
            pst.setString(9, total);

            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Ticket Booked Successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Error!");
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Enter valid Details!");
    }
        
        
    }//GEN-LAST:event_btnBookActionPerformed

